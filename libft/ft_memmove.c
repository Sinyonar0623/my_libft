#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	char			j;
	size_t			i;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	j = 1;
	i = 0;
	if (!dest && !src)
		return (dest);
	if (dest > src)
	{
		j = -1;
		d += n - 1;
		s += n - 1;
	}
	while (i < n)
	{
		*d = *s;
		d += j;
		s += j;
		i++;
	}
	return (dest);
}
