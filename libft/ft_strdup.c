#include "libft.h"

char	*ft_strdup(const char *s)
{
	char		*ptr;
	size_t		i;

	if (!s)
		return (NULL);
	i = ft_strlen(s) + 1;
	ptr = (char *)malloc(i * sizeof(*s));
	if (!ptr)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
